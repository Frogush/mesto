// Подключаем плагин Autoprefixer — автоматически добавляет вендорные префиксы
// для обеспечения кроссбраузерной поддержки CSS (например, -webkit-, -ms-)
const autoprefixer = require("autoprefixer");

// Подключаем плагин cssnano — минифицирует CSS (удаляет пробелы, комментарии и т.д.)
// Это помогает уменьшить размер итогового CSS-файла
const cssnano = require("cssnano");

// Экспортируем конфигурацию PostCSS
module.exports = {
  // Указываем список подключаемых плагинов
  plugins: [
    // Автоматическая расстановка префиксов в стилях
    autoprefixer,
    // Минификация CSS с настройкой "preset: default" — стандартный набор оптимизаций
    cssnano({ preset: "default" }),
  ],
};
